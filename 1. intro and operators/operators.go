package main

import "fmt"

func main() {
	a := 4
	b := 6

	fmt.Println(a + b)
	fmt.Println(a - b)
	fmt.Println(a * b)
	fmt.Println(a / b)
	fmt.Println(a == b)
	fmt.Println(a != b)
	fmt.Println(a > b)
	fmt.Println(a < b)
	a++
	fmt.Println(a)
	b--
	fmt.Println(b)

}

